def convert_font(text, font_map):
    """–ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —Ç–µ–∫—Å—Ç –≤ —É–∫–∞–∑–∞–Ω–Ω—ã–π —à—Ä–∏—Ñ—Ç"""
    if not font_map:  # –ï—Å–ª–∏ —à—Ä–∏—Ñ—Ç –ø—É—Å—Ç–æ–π, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã–π —Ç–µ–∫—Å—Ç
        return text
    
    result = []
    for char in text:
        if char in font_map:
            result.append(font_map[char])
        else:
            result.append(char)
    return ''.join(result)

def get_font_keyboard(user_id, page=0):
    """–°–æ–∑–¥–∞–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å —à—Ä–∏—Ñ—Ç–∞–º–∏ –¥–ª—è —Å—Ç—Ä–∞–Ω–∏—Ü—ã"""
    fonts_per_page = 5
    font_keys = list(FONTS.keys())
    total_pages = (len(font_keys) + fonts_per_page - 1) // fonts_per_page
    
    # –ü–æ–ª—É—á–∞–µ–º —à—Ä–∏—Ñ—Ç—ã –¥–ª—è —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    start_idx = page * fonts_per_page
    end_idx = start_idx + fonts_per_page
    page_fonts = font_keys[start_idx:end_idx]
    
    keyboard = []
    
    # –ö–Ω–æ–ø–∫–∏ —à—Ä–∏—Ñ—Ç–æ–≤
    for font_key in page_fonts:
        font_data = FONTS[font_key]
        is_selected = user_fonts.get(user_id) == font_key
        emoji = "‚úÖ" if is_selected else "üî§"
        keyboard.append([InlineKeyboardButton(
            f"{emoji} {font_data['name']}", 
            callback_data=f"select_{font_key}_{page}"
        )])
    
    # –ö–Ω–æ–ø–∫–∏ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
    nav_buttons = []
    if page > 0:
        nav_buttons.append(InlineKeyboardButton("‚¨ÖÔ∏è –ù–∞–∑–∞–¥", callback_data=f"page_{page-1}"))
    
    nav_buttons.append(InlineKeyboardButton(f"{page+1}/{total_pages}", callback_data="ignore"))
    
    if page < total_pages - 1:
        nav_buttons.append(InlineKeyboardButton("–í–ø–µ—Ä—ë–¥ ‚û°Ô∏è", callback_data=f"page_{page+1}"))
    
    if nav_buttons:
        keyboard.append(nav_buttons)
    
    return InlineKeyboardMarkup(keyboard)

